package CAR;
public class CarRider {

    private String nama;
    private int age;
    private String phone;


    public CarRider(String nama, int age, String phone) {
        this.nama = nama;
        this.age = age;
        this.phone = phone;
    }


    public String getNama() {
        return nama;
    }


    public void setNama(String nama) {
        this.nama = nama;
    }


    public int getAge() {
        return age;
    }


    public void setAge(int age) {
        this.age = age;
    }


    public String getPhone() {
        return phone;
    }


    public void setPhone(String phone) {
        this.phone = phone;
    }

   
}
